import { readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { describe, expect, it } from 'vitest';

const root = join(dirname(fileURLToPath(import.meta.url)), '..', '..', '..');
const resultPage = readFileSync(join(root, 'app', 'result', '[requestId]', 'page.tsx'), 'utf8');
const pagination = readFileSync(join(root, 'components', 'result-pagination.tsx'), 'utf8');
const presentation = readFileSync(
    join(root, 'lib', 'services', 'analysis', 'owner-view-presentation.ts'),
    'utf8',
);

describe('result page pagination copy contract', () => {
    it('drops the range/plus counters and the old load-more buttons', () => {
        for (const forbidden of [
            'paginatedRangeLabel',
            'paginatedCountLabel',
            '더 보기',
            '이전 목록',
            'ResultPaginationControls',
        ]) {
            expect(resultPage).not.toContain(forbidden);
        }
        // A "1-50+" style range is generated by combining a hyphen range with a
        // trailing "+"; neither the helpers nor that shape should survive.
        expect(resultPage).not.toMatch(/\d+-\d+\+/);
    });

    it('removes the range/count label helpers from the presentation module', () => {
        expect(presentation).not.toContain('paginatedRangeLabel');
        expect(presentation).not.toContain('paginatedCountLabel');
    });

    it('marks the active page with aria-current in the numbered pager', () => {
        expect(pagination).toContain("aria-current={item.current ? 'page' : undefined}");
    });

    it('keeps accessible gender text labels while icons are decorative', () => {
        expect(resultPage).toContain("from 'lucide-react'");
        expect(resultPage).toContain('OWNER_GENDER_LABELS.male');
        expect(resultPage).toContain('OWNER_GENDER_LABELS.female');
        expect(resultPage).toContain('OWNER_GENDER_LABELS.unknown');
        // The gender icons must be aria-hidden so the retained text carries meaning.
        expect(resultPage).toContain('<row.Icon aria-hidden="true"');
    });

    it('keeps the signed image proxy boundary for profile images', () => {
        expect(resultPage).toContain("url.startsWith('/api/image-proxy?')");
    });
});
